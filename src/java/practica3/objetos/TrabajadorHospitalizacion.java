public class TrabajadorHospitalizacion {
    
    private int id, id_empleado, id_historial_medico;
    private String area_trabajador;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_empleado() {
        return id_empleado;
    }

    public void setId_empleado(int id_empleado) {
        this.id_empleado = id_empleado;
    }

    public int getId_historial_medico() {
        return id_historial_medico;
    }

    public void setId_historial_medico(int id_historial_medico) {
        this.id_historial_medico = id_historial_medico;
    }

    public String getArea_trabajador() {
        return area_trabajador;
    }

    public void setArea_trabajador(String area_trabajador) {
        this.area_trabajador = area_trabajador;
    }
    
    
}
